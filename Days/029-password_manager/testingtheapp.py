import sys

from PyQt5.QtWidgets import QApplication, QLabel, QWidget


def test_app():
    app = QApplication(sys.argv)
    w = QWidget()
    w.resize(250, 150)
    w.setWindowTitle("Simple Test")
    label = QLabel(w)
    label.setText("This is a test label.")
    label.move(50, 50)
    w.show()
    sys.exit(app.exec_())


test_app()
