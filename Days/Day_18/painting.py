screen = Screen()
tim = Turtle()
tim.shape("circle")
tim.color("red")
tim.pen(pensize=20)
tim.speed("fastest")
tim.ht()
screen.colormode(255)
screen.screensize(500, 500)


def walk():
    tim.penup()
    tim.goto(-250, -250)
    up = 0
    left = 0
    while up < 10:
        while left < 10:
            tim.pendown()
            tim.dot(30, random.choice(color_list))
            tim.penup()
            tim.forward(60)
            left += 1
        up += 1
        left = 0
        tim.goto(-250, -250 + (60 * up))


walk()
