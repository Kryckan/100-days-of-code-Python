import requests


class ISSPosition:
    def __init__(self):
        self.api_url = "http://api.open-notify.org/iss-now.json"

    def get_iss_position(self):
        response = requests.get(url=self.api_url)
        response.raise_for_status()
        data = response.json()
        return data
